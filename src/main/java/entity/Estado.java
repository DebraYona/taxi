package entity;

public class Estado {
    int idestado;
    String nombreEstado;

    public int getEstado() {
        return idestado;
    }

    public void setEstado(int estado) {
        this.idestado = estado;
    }

    public String getNombreEstado() {
        return nombreEstado;
    }

    public void setNombreEstado(String nombreEstado) {
        this.nombreEstado = nombreEstado;
    }


}
